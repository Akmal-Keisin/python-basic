# Assigning variable
hello = "Hello World"
print(hello)

number = 123
print (number)

float = 12.34
print (float)

# Multiple assignment
a, b, c = 1, 2, 3
print(a)
print(b)
print(c)

# Change variable value
a = 10
print(a)
a = "Now I'm a string"
print(a)

# Python is dynamic typed language

# Integer
numberInteger = 42
print("Integer:", numberInteger)
print(type(numberInteger))

# Float
numberFloat = 3.14159
print("Float:", numberFloat)
print(type(numberFloat))

# String
greetingString = "Hello, Python!"
print("String:", greetingString)
print(type(greetingString))

# Multi line string
poetry = """Roses are red,
Violets are blue,
Python is great,
And so are you."""
print("Multi-line String:\n", poetry)
print(type(poetry))

# Boolean
isPythonFun = True
print("Boolean:", isPythonFun)
print(type(isPythonFun))