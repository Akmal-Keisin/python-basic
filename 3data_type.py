# Python is dynamic typed language

# Integer
numberInteger = 42
print("Integer:", numberInteger)
print(type(numberInteger))

# Float
numberFloat = 3.14159
print("Float:", numberFloat)
print(type(numberFloat))

# String
greetingString = "Hello, Python!"
print("String:", greetingString)
print(type(greetingString))

# Multi line string
poetry = """Roses are red,
Violets are blue,
Python is great,
And so are you."""
print("Multi-line String:\n", poetry)
print(type(poetry))

# Boolean
isPythonFun = True
print("Boolean:", isPythonFun)
print(type(isPythonFun))