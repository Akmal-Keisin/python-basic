# Convert string to integer for arithmetic operations
firstNumber = input("Enter the first number: ")
secondNumber = input("Enter the second number: ")

# Convert firstNumber and secondNumber to integers
firstNumberInt = int(firstNumber)
secondNumberInt = int(secondNumber)

# Perform addition
sumResult = firstNumberInt + secondNumberInt
print("The sum of", firstNumberInt, "and", secondNumberInt, "is:", sumResult)

