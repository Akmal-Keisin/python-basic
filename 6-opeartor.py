# Practice: Operators

# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

# Comparison Operators
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater Than:", a > b)
print("Less Than:", a < b)
print("Greater Than or Equal:", a >= b)
print("Less Than or Equal:", a <= b)

# Logical Operators
x = True
y = False

print("Logical AND:", x and y)
print("Logical OR:", x or y)
print("Logical NOT:", not x)

# String Operators
str1 = "Hello"
str2 = "World"

print("Concatenation:", str1 + " " + str2)
print("Repetition:", str1 * 3)
print("Membership (H in Hello):", 'H' in str1)
print("Membership (z in World):", 'z' in str2)