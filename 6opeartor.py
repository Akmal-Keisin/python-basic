# Aritmathic Operators
a = 10
b = 3

# Addition
print("Addition:", a + b)
# Subtraction
print("Subtraction:", a - b)
# Multiplication
print("Multiplication:", a * b)
# Division
print("Division:", a / b)
# Floor Division
print("Floor Division:", a // b)
# Modulus
print("Modulus:", a % b)
# Exponentiation
print("Exponentiation:", a ** b)

# Comparison Operators

# Equal
print("Equal:", a == b)
# Not Equal
print("Not Equal:", a != b)
# Greater Than
print("Greater Than:", a > b)
# Less Than
print("Less Than:", a < b)
# Greater Than or Equal
print("Greater Than or Equal:", a >= b)
# Less Than or Equal
print("Less Than or Equal:", a <= b)

# Logical Operators
x = True
y = False

# AND
print("Logical AND:", x and y)
# OR
print("Logical OR:", x or y)
# NOT
print("Logical NOT:", not x)

# String Operators
str1 = "Hello"
str2 = "World"

# Concatenation
print("Concatenation:", str1 + " " + str2)
# Repetition
print("Repetition:", str1 * 3)
# Membership
print("Membership (H in Hello):", 'H' in str1)
print("Membership (z in World):", 'z' in str2)