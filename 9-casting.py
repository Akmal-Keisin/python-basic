# Practice: Casting

# Casting to Integer
intNum1 = int(12)
intNum2 = int(12.2)
intNum3 = int("12")

print(intNum1)
print(intNum2)
print(intNum3)

# Casting to Float
floatNum1 = float(8)
floatNum2 = float(92.2)
floatNum3 = float("23.2")
floatNum4 = float("3")

print(floatNum1)
print(floatNum2)
print(floatNum3)
print(floatNum4)

# Casting to String
stringWord1 = str("s1")
stringWord2 = str(2)
stringWord3 = str(3.0)

print(stringWord1)
print(stringWord2)
print(stringWord3)